import React, { useState, useEffect, useContext, useRef } from "react";
import axios from 'axios';
import { Link, useNavigate } from 'react-router-dom';
import '../styles/Container.css';
import Logo from '../assets/Logo.png';
import {
  Container,
  Box,
  Snackbar,
  Alert,
  TextField,
  Modal,
} from "@mui/material";
import {
  Email as EmailIcon,
  Lock as LockIcon,
  Visibility,
  VisibilityOff
} from "@mui/icons-material";
// import ReCAPTCHA from "react-google-recaptcha";
import { SettingsContext } from "../App"; // âœ… Access settings from context
import API_BASE_URL from "../apiConfig";
const Register = () => {
  const settings = useContext(SettingsContext);

  const [titleColor, setTitleColor] = useState("#000000");
  const [subtitleColor, setSubtitleColor] = useState("#555555");
  const [borderColor, setBorderColor] = useState("#000000");
  const [mainButtonColor, setMainButtonColor] = useState("#1976d2");


  useEffect(() => {
    if (settings) {
      if (settings.title_color) setTitleColor(settings.title_color);
      if (settings.subtitle_color) setSubtitleColor(settings.subtitle_color);
      if (settings.border_color) setBorderColor(settings.border_color);
      if (settings.main_button_color) setMainButtonColor(settings.main_button_color);

    }
  }, [settings]);

  // const [capVal, setCapVal] = useState(null);
  const [usersData, setUserData] = useState({
    email: '',
    password: '',
  });
  const [showPassword, setShowPassword] = useState(false);
  const [confirmPassword, setConfirmPassword] = useState("");
  const [snack, setSnack] = useState({ open: false, message: '', severity: 'info' });
  const [otp, setOtp] = useState("");
  const [showOtpModal, setShowOtpModal] = useState(false);
  const [resendTimer, setResendTimer] = useState(180);
  const [loadingOtp, setLoadingOtp] = useState(false);
  const otpInputRef = useRef(null);
  const [tempEmail, setTempEmail] = useState("");
  const navigate = useNavigate();

  const handleChanges = (e) => {
    const { name, value } = e.target;
    setUserData(prevState => ({ ...prevState, [name]: value }));
  };

  const handleClose = (_, reason) => {
    if (reason === 'clickaway') return;
    setSnack(prev => ({ ...prev, open: false }));
  };

  const [isSubmitting, setIsSubmitting] = useState(false);

  const handleRegister = async () => {
    if (isSubmitting) return;

    // âœ… Gmail-only validation
    if (!usersData.email.endsWith("@gmail.com")) {
      setSnack({
        open: true,
        message: "Only Gmail addresses are allowed!",
        severity: "error",
      });
      return;
    }

    // âœ… Password match check
    if (usersData.password !== confirmPassword) {
      setSnack({
        open: true,
        message: "Passwords do not match!",
        severity: "error",
      });
      return;
    }

    setIsSubmitting(true);
    try {
      await axios.post(`${API_BASE_URL}/request-otp`, {
        email: usersData.email,
      });

      setTempEmail(usersData.email);
      setShowOtpModal(true);
      startResendTimer();

      setSnack({
        open: true,
        message: "OTP sent to your email",
        severity: "success",
      });

      setIsSubmitting(false);
      return;

    } catch (error) {
      setSnack({
        open: true,
        message: error.response?.data?.message || "Failed to send OTP",
        severity: "error",
      });

      setIsSubmitting(false);
      return;
    }

  };

  useEffect(() => {
    if (showOtpModal && otpInputRef.current) {
      otpInputRef.current.focus(); // Automatically focuses input
    }
  }, [showOtpModal]);

  const startResendTimer = () => {
    setResendTimer(120);
    const interval = setInterval(() => {
      setResendTimer((prev) => {
        if (prev <= 1) {
          clearInterval(interval);
          return 0;
        }
        return prev - 1;
      });
    }, 1000);
  };

  const resendOtp = async () => {
    try {
      setLoadingOtp(true);

      await axios.post(`${API_BASE_URL}/request-otp`, {
        email: tempEmail,
      });

      startResendTimer();
      setSnack({ open: true, message: "OTP resent!", severity: "success" });
    } catch (err) {
      setSnack({
        open: true,
        message: err.response?.data?.message || "Failed to resend OTP",
        severity: "error",
      });
    }

    setLoadingOtp(false);
  };

  const verifyOtp = async () => {
    setLoadingOtp(true);
    try {
      const response = await axios.post(`${API_BASE_URL}/register`, {
        ...usersData,
        otp,
      });

      if (!response.data.success) {
        // Show error message if registration failed
        setSnack({
          open: true,
          message: response.data.message,
          severity: "error",
        });
        return;
      }

      // Success message: highlight "one-time registration"
      setSnack({
        open: true,
        message: "ðŸŽ‰ Congratulations! You just created an account. Note: you can only register once with this email.",
        severity: "success",
      });

      // Close OTP modal
      setShowOtpModal(false);

      // Delay navigation so user can read snackbar
      setTimeout(() => navigate("/login_applicant"), 1500);

    } catch (err) {
      setSnack({
        open: true,
        message: err.response?.data?.message || "Something went wrong. Please try again.",
        severity: "error",
      });
    } finally {
      setLoadingOtp(false);
    }
  };

  const handleKeyDownRegister = (e) => {
    if (e.key === "Enter" && !isSubmitting) {
      handleRegister();
    }
  };

  const handleKeyDownOtp = (e) => {
    if (e.key === "Enter" && !loadingOtp) {
      verifyOtp();
    }
  };


  // âœ… Use background from settings or fallback image
  const backgroundImage = settings?.bg_image
    ? `url(${API_BASE_URL}${settings.bg_image})`
    : "url(/default-bg.jpg)";

  return (
    <>
      <Box
        sx={{
          backgroundImage,
          backgroundSize: "cover",
          backgroundPosition: "center",
          backgroundRepeat: "no-repeat",
          width: "100%",
          minHeight: "100vh",
          display: "flex",
          alignItems: "center",
          justifyContent: "center",
        }}
      >
        <Container
          style={{
            display: "flex",
            alignItems: "center",
            justifyContent: "center",
          }}
          maxWidth={false}
        >
          <div style={{ border: "5px solid black" }} className="Container">
            <div
              className="Header"
              style={{
                backgroundColor: settings?.header_color || "#1976d2", // âœ… default blue
                padding: "1rem 0",
                borderBottom: "3px solid black",
              }}
            >

              <div className="HeaderTitle">
                <div className="CircleCon">
                  <img
                    src={
                      settings?.logo_url
                        ? `${API_BASE_URL}${settings.logo_url}`
                        : Logo
                    }
                    alt="Logo"
                  />
                </div>
              </div>
              <div className="HeaderBody">
                <strong style={{
                  color: "white",
                }}>{settings?.company_name || "Company Name"}</strong>
                <p>Student Information System</p>
              </div>
            </div>

            <div className="Body">
              <div className="TextField" style={{ position: "relative" }}>
                <label htmlFor="email">Email Address</label>
                <input
                  type="email"
                  className="border"
                  id="email"
                  name="email"
                  placeholder="Enter your email address"
                  value={usersData.email}
                  onChange={handleChanges}
                  onKeyDown={handleKeyDownRegister}
                  style={{ paddingLeft: "2.5rem", border: `2px solid ${borderColor}` }}
                />
                <EmailIcon
                  style={{
                    position: "absolute",
                    top: "2.5rem",
                    left: "0.7rem",
                    color: "rgba(0,0,0,0.4)"
                  }}
                />
              </div>

              <div className="TextField" style={{ position: "relative" }}>
                <label htmlFor="password">Password</label>
                <input
                  type={showPassword ? "text" : "password"}
                  className="border"
                  id="password"
                  name="password"
                  placeholder="Enter your password"
                  value={usersData.password}
                  onChange={handleChanges}
                  onKeyDown={handleKeyDownRegister}
                  required
                  style={{ paddingLeft: "2.5rem", border: `2px solid ${borderColor}` }}
                />
                <LockIcon
                  style={{
                    position: "absolute",
                    top: "2.5rem",
                    left: "0.7rem",
                    color: "rgba(0,0,0,0.4)"
                  }}
                />
                <button
                  type="button"
                  onClick={() => setShowPassword(!showPassword)}
                  style={{
                    color: "rgba(0,0,0,0.3)",
                    outline: "none",
                    position: "absolute",
                    top: "2.5rem",
                    right: "1rem",
                    background: "none",
                    border: "none",
                    cursor: "pointer"
                  }}
                >
                  {showPassword ? <Visibility /> : <VisibilityOff />}
                </button>
              </div>

              <div className="TextField" style={{ position: "relative" }}>
                <label htmlFor="confirmPassword">Confirm Password</label>
                <input
                  type={showPassword ? "text" : "password"}
                  className="border"
                  id="confirmPassword"
                  name="confirmPassword"
                  placeholder="Re-enter your password"
                  value={confirmPassword}
                  onChange={(e) => setConfirmPassword(e.target.value)}
                  onKeyDown={handleKeyDownRegister}
                  required
                  disabled={!usersData.password} // âœ… Disabled until password is filled
                  style={{
                    paddingLeft: "2.5rem",
                    border: `2px solid ${borderColor}`,
                    backgroundColor: !usersData.password ? "#f0f0f0" : "white", // Optional: gray background when disabled
                    cursor: !usersData.password ? "not-allowed" : "text",
                  }}
                />
                <LockIcon
                  style={{
                    position: "absolute",
                    top: "2.5rem",
                    left: "0.7rem",
                    color: "rgba(0,0,0,0.4)"
                  }}
                />
                <button
                  type="button"
                  onClick={() => setShowPassword(!showPassword)}
                  style={{
                    color: "rgba(0,0,0,0.3)",
                    outline: "none",
                    position: "absolute",
                    top: "2.5rem",
                    right: "1rem",
                    background: "none",
                    border: "none",
                    cursor: "pointer"
                  }}
                >
                  {showPassword ? <Visibility /> : <VisibilityOff />}
                </button>
              </div>


              {/* CAPTCHA */}
              {/* <Box sx={{ display: 'flex', justifyContent: 'center', marginTop: '20px' }}>
                <ReCAPTCHA
                  sitekey="6Lfem44rAAAAAEeAexdQxvN0Lpm1V4KPu1bBxaGy"
                  onChange={(val) => setCapVal(val)}
                />
              </Box> */}

              {/* Register Button â€” disabled until CAPTCHA is solved */}
              <div
                onClick={!isSubmitting ? handleRegister : null}
                style={{
                  pointerEvents: !isSubmitting ? "auto" : "none",
                  opacity: !isSubmitting ? 1 : 0.5,
                  cursor: !isSubmitting ? "pointer" : "not-allowed",
                  marginTop: "20px",
                  backgroundColor: mainButtonColor,
                  height: "50px",
                  border: `2px solid ${borderColor}`,
                  borderRadius: "10px",
                  display: "flex",
                  alignItems: "center",
                  justifyContent: "center",
                  color: "white",
                  fontWeight: "bold",
                  fontSize: "16px",
                }}
              >
                {isSubmitting ? "Registering..." : "Register"}
              </div>


              <div className="LinkContainer RegistrationLink" style={{ margin: '0.1rem 0rem' }}>
                <p>Already Have an Account?</p>
                <span><Link to={'/login_applicant'}>Sign In here</Link></span>
              </div>
            </div>

            <div className="Footer">
              <div className="FooterText">
                &copy; 2025 {settings?.company_name || "EARIST"} Student Information System. All rights reserved.
              </div>
            </div>
          </div>
        </Container>

        <Modal open={showOtpModal} onClose={() => setShowOtpModal(false)}>
          <Box
            sx={{
              position: "absolute",
              top: "50%",
              left: "50%",
              transform: "translate(-50%, -50%)",
              bgcolor: "#fff",
              p: 4,
              border: "3px solid black",
              borderRadius: "12px",
              width: 350,
              height: 350,
              boxShadow: 24,
              textAlign: "center",
              position: "relative",
            }}
          >
            <button
              onClick={() => setShowOtpModal(false)}
              style={{
                position: "absolute",
                top: "8px",
                right: "8px",
                backgroundColor: "#6D2323",
                color: "white",
                border: "none",
                borderRadius: "50%",
                width: "32px",
                height: "32px",
                cursor: "pointer",
              }}
            >
              âœ•
            </button>

            <h2 style={{ color: "#6D2323", marginBottom: "10px", fontSize: "16px" }}>
              Enter the 6-digit OTP<br />
              <small style={{ color: "#555", fontWeight: "normal", fontSize: "16px" }}>
                Note: This email can only be used to register once.
              </small>
            </h2>

            <TextField
              fullWidth
              value={otp}
              onChange={(e) => setOtp(e.target.value)}
              placeholder="Enter OTP"
              inputRef={otpInputRef}
              onKeyDown={handleKeyDownOtp} // Submit on Enter
              inputProps={{
                maxLength: 6,
                style: { textAlign: "center", fontSize: "18px" },
              }}
              sx={{ mb: 2 }}
            />

            <button
              onClick={verifyOtp}
              disabled={loadingOtp}
              style={{
                width: "100%",
                backgroundColor: mainButtonColor,
                color: "white",
                padding: "10px",
                borderRadius: "6px",
                border: "none",
                fontWeight: "bold",
              }}
            >
              {loadingOtp ? "Verifying..." : "Verify OTP"}
            </button>

            <button
              onClick={resendOtp}
              disabled={resendTimer > 0}
              style={{
                marginTop: "10px",
                width: "100%",
                padding: "10px",
                borderRadius: "6px",
                border: "2px solid black",
                background: "white",
                cursor: resendTimer > 0 ? "not-allowed" : "pointer",
              }}
            >
              {resendTimer > 0 ? `Resend in ${resendTimer}s` : "Resend OTP"}
            </button>
          </Box>
        </Modal>



        {/* Snackbar Notification */}
        <Snackbar
          open={snack.open}
          autoHideDuration={4000}
          onClose={handleClose}
          anchorOrigin={{ vertical: 'top', horizontal: 'center' }}
        >
          <Alert severity={snack.severity} onClose={handleClose} sx={{ width: '100%' }}>
            {snack.message}
          </Alert>
        </Snackbar>
      </Box>
    </>
  );
};

export default Register;
